#include "header.h"

int	ft_check_op(char *s)
{
	if (s[1] != '\0')
		return (-1);
	if (s[0] == '+')
		return (0);
	if (s[0] == '-')
		return (1);
	if (s[0] == '*')
		return (2);
	if (s[0] == '/')
		return (3);
	if (s[0] == '%')
		return (4);
	return (-1);
}
