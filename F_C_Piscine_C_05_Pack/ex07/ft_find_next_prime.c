int	ft_is_prime(int nb)
{
	int	counter;

	counter = 2;
	while (counter < nb)
	{	
		if (nb % counter == 0)
			return (0);
		counter++;
	}
	return (1);
}

int	ft_find_next_prime(int nb)
{
	if (nb <= 2)
		return (2);
	while (!ft_is_prime(nb))
		nb++;
	return (nb);
}
